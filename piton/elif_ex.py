#exemplo 1
n_alu = int(input("Digite o numeros de alunos na sua turma: "))
if n_alu > 40:
    print("Sua turma é muito grande!")
elif n_alu < 40:
    print("Sua turma é muito pequena!")
else: 
    print("Sua turma tem tamanho aceitavel!")


